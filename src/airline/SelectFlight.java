    private void btnBookTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookTicketActionPerformed
            int selectedRow = tblFlights.getSelectedRow();

    if(selectedRow == -1){
        JOptionPane.showMessageDialog(this, "Please select a flight to book");
        return;
    }

    // 1️⃣ Get flight info and seats from the table
    int flight_id = (int) tblFlights.getValueAt(selectedRow, 0);
    String flight_name = (String) tblFlights.getValueAt(selectedRow, 1);
    String source = (String) tblFlights.getValueAt(selectedRow, 2);
    String destination = (String) tblFlights.getValueAt(selectedRow, 3);
    String datetime = tblFlights.getValueAt(selectedRow, 4).toString();
    int business_seats = Integer.parseInt(tblFlights.getValueAt(selectedRow, 5).toString());
    int economy_seats = Integer.parseInt(tblFlights.getValueAt(selectedRow, 6).toString());
    

    // 2️⃣ Get prices from the database (this is where you add your code)
    double economy_price = 0, business_price = 0;
    try {
            Class.forName("oracle.jdbc.OracleDriver");
    		Connection con = DriverManager.getConnection(
        	"jdbc:oracle:thin:@//localhost:1522/XEPDB1", "system", "oracle");
        PreparedStatement pst = con.prepareStatement(
            "SELECT economy_price, business_price FROM flight WHERE flight_id=?");
        pst.setInt(1, flight_id);
        ResultSet rs = pst.executeQuery();
        if(rs.next()) {
            economy_price = rs.getDouble("economy_price");
            business_price = rs.getDouble("business_price");
        }
        con.close();
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }

    // 3️⃣ Open BookTicket window with all info
    new BookTicket(flight_id, flight_name, source, destination, datetime,
               business_seats, economy_seats, business_price, economy_price)
               .setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnBookTicketActionPerformed

