void loadFlights() {

    try {
        Class.forName("oracle.jdbc.OracleDriver");

        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@//localhost:1522/XEPDB1",
            "system",
            "oracle"
        );

        String sql = "SELECT flight_id, flight_name, source, destination, " +
                     "departure_datetime, business_seats, business_price, " +
                     "economy_seats , economy_price" +
                     " FROM flight ";

        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model =
            (DefaultTableModel) tblFlights.getModel();

        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("flight_id"),
                rs.getString("flight_name"),
                rs.getString("source"),
                rs.getString("destination"),
                rs.getTimestamp("departure_datetime"),
                rs.getInt("business_seats"),
                rs.getInt("economy_seats")
            });
        }

        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }
}
